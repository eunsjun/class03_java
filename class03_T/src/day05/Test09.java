package day05;

public class Test09 {

	public static void main(String[] args) {
		// 반복문을 사용해서 11 ~ 20까지 출력하세요.
		// for
		for(int i = 11; i < 21 ; i++) {
			System.out.print(i + ", ");
		}
		
		System.out.println();
		// while
		int j = 11;
		while(j < 21) {
			System.out.print(j + ", ");
			j++;
		}
		
	}

}
